const solve = require("./part1");

describe(solve, () => {
  it("solves the example", () => {
    const example = [
      "............",
      "........0...",
      ".....0......",
      ".......0....",
      "....0.......",
      "......A.....",
      "............",
      "............",
      "........A...",
      ".........A..",
      "............",
      "............",
    ].join("\n");

    expect(solve(example)).toEqual(14);
  });
});
